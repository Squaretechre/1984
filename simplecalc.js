module.exports = {
  add: function add(num1, num2) {
    return num1 + num2;
  },

  subtract: function subtract(num1, num2) {
    return num1 - num2;
  },

  divide: function divide(num1, num2) {
    return num1 / num2;
  },

  multiply: function multiply(num1, num2) {
    return num1 * num2;
  },

  modulo: function modulo(num1, num2) {
    return num1 % num2;
  },
};
