module.exports = {
  add: function add(number1, number2) {
    return number1 + number2;
  },

  subtract: function subtract(number1, number2) {
    return number1 - number2;
  },

  divide: function divide(number1, number2) {
    return number1 / number2;
  },

  multiply: function multiply(num1, num2) {
    return num1 * num2;
  },
};
